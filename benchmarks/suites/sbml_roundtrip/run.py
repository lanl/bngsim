#!/usr/bin/env python3
"""Generate BNG-roundtrip SBMLs for the SSA model corpus.

For each `bngsim/benchmarks/models/bngl/ssa/*.bngl`, this script:

  1. Writes a wrapper `.bngl` in a temp dir whose actions block reads
     the original with ``skip_actions=>1``, then runs
     ``generate_network({overwrite=>1})`` and ``writeSBML()``.
     ``skip_actions`` is what keeps BNG from running the source file's
     ``simulate({method=>"ssa", ...})`` action — that simulate would
     otherwise run a full Gillespie sweep before writeSBML, which is
     wasted compute (especially for fceri_gamma / egfr_net).
  2. Invokes BNG2.pl on the wrapper inside that temp dir.
  3. Copies ``<name>_sbml.xml`` to
     ``benchmarks/suites/sbml_roundtrip/<name>.xml``, prepending an XML
     comment with the BNG version and source bngl for traceability.

The generated ``*.xml`` and ``load_check.json`` are git-ignored
regenerated artifacts -- run this script to (re)materialise them.

Idempotency: a given ``<name>.xml`` is regenerated only when older
than its source ``.bngl`` or the configured BNG2.pl. Pass ``--force``
to regenerate everything.

The script also runs a loader smoke test (``Model.from_sbml`` per file)
and writes ``load_check.json`` in the same directory. Pass
``--no-load-check`` to skip, or ``--no-generate`` to only run the load
check against an existing corpus.
"""

from __future__ import annotations

import argparse
import datetime as _dt
import json
import subprocess
import sys
import tempfile
import time
import warnings
from pathlib import Path

HERE = Path(__file__).resolve().parent
# HERE is benchmarks/suites/sbml_roundtrip/; the model store is two
# levels up at benchmarks/models/.
SSA_BNGL_DIR = HERE.parents[1] / "models" / "bngl" / "ssa"
DEFAULT_BNG_PATH = Path("~/Simulations/BioNetGen-2.9.3/BNG2.pl").expanduser()


WRAPPER_TEMPLATE = """\
# Auto-generated by bngsim/benchmarks/suites/sbml_roundtrip/run.py.
# Reads the source .bngl with skip_actions=>1 so that the source's
# simulate({{method=>"ssa", ...}}) action does not execute, then
# expands the network and emits SBML.
begin actions
  readFile({{file=>"{src}", skip_actions=>1}})
  generate_network({{overwrite=>1}})
  writeSBML()
end actions
"""


def detect_bng_version(bng_path: Path) -> str:
    out = subprocess.run(
        [str(bng_path), "--version"],
        capture_output=True,
        text=True,
        check=True,
    ).stdout.strip()
    return out or "BioNetGen (unknown version)"


def needs_regen(src: Path, dst: Path, bng_path: Path) -> bool:
    if not dst.exists():
        return True
    dst_mtime = dst.stat().st_mtime
    if src.stat().st_mtime > dst_mtime:
        return True
    return bng_path.exists() and bng_path.stat().st_mtime > dst_mtime


def generate_one(
    src: Path,
    dst: Path,
    bng_path: Path,
    bng_version: str,
) -> dict:
    """Run BNG on a wrapper and copy the resulting SBML to ``dst``.

    Returns a result dict for the load_check.json sibling.
    """
    name = src.stem  # e.g. "simple_system"
    started = time.time()
    with tempfile.TemporaryDirectory(prefix="bng_sbml_") as td:
        td_path = Path(td)
        wrapper = td_path / f"{name}.bngl"
        wrapper.write_text(WRAPPER_TEMPLATE.format(src=src.resolve()))
        completed = subprocess.run(
            [str(bng_path), wrapper.name],
            cwd=td_path,
            capture_output=True,
            text=True,
        )
        elapsed = time.time() - started
        if completed.returncode != 0:
            return {
                "bng_writeSBML_ok": False,
                "bng_returncode": completed.returncode,
                "bng_stderr_tail": _tail(completed.stderr, 30),
                "bng_stdout_tail": _tail(completed.stdout, 30),
                "elapsed_sec": round(elapsed, 3),
            }
        produced = td_path / f"{name}_sbml.xml"
        if not produced.exists():
            # BNG silently failed to emit SBML — surface its log.
            return {
                "bng_writeSBML_ok": False,
                "bng_returncode": 0,
                "bng_stderr_tail": _tail(completed.stderr, 30),
                "bng_stdout_tail": _tail(completed.stdout, 30),
                "elapsed_sec": round(elapsed, 3),
            }
        body = produced.read_text()
        # Prepend a traceability comment immediately after the XML decl.
        decl, _, rest = body.partition("\n")
        header = (
            f"<!-- Generated by bngsim/benchmarks/suites/sbml_roundtrip/run.py -->\n"
            f"<!-- Source: benchmarks/models/bngl/ssa/{src.name} -->\n"
            f"<!-- BNG: {bng_version} -->\n"
        )
        dst.parent.mkdir(parents=True, exist_ok=True)
        dst.write_text(decl + "\n" + header + rest)
    return {
        "bng_writeSBML_ok": True,
        "bng_returncode": 0,
        "elapsed_sec": round(elapsed, 3),
    }


def _tail(text: str, n: int) -> list[str]:
    lines = (text or "").splitlines()
    return lines[-n:]


def load_check_one(xml_path: Path) -> dict:
    from bngsim import Model  # imported lazily so --no-load-check skips this dep

    started = time.time()
    with warnings.catch_warnings(record=True) as ws:
        warnings.simplefilter("always")
        try:
            Model.from_sbml(xml_path)
        except Exception as exc:
            return {
                "from_sbml_status": "error",
                "from_sbml_error": f"{type(exc).__name__}: {exc}",
                "from_sbml_warnings": [str(w.message) for w in ws],
                "loader_warning_count": len(ws),
                "elapsed_sec": round(time.time() - started, 3),
            }
    return {
        "from_sbml_status": "warn" if ws else "pass",
        "from_sbml_error": None,
        "from_sbml_warnings": [str(w.message) for w in ws],
        "loader_warning_count": len(ws),
        "elapsed_sec": round(time.time() - started, 3),
    }


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        "--bng-path",
        type=Path,
        default=DEFAULT_BNG_PATH,
        help="Path to BNG2.pl (default: ~/Simulations/BioNetGen-2.9.3/BNG2.pl)",
    )
    p.add_argument(
        "--ssa-dir",
        type=Path,
        default=SSA_BNGL_DIR,
        help="Directory containing source SSA *.bngl files.",
    )
    p.add_argument(
        "--out-dir",
        type=Path,
        default=HERE,
        help="Directory to write generated *.xml + load_check.json.",
    )
    p.add_argument(
        "--force", action="store_true", help="Regenerate every SBML even if it appears up-to-date."
    )
    p.add_argument(
        "--no-generate",
        action="store_true",
        help="Skip BNG generation; only run loader smoke test.",
    )
    p.add_argument(
        "--no-load-check",
        action="store_true",
        help="Skip Model.from_sbml smoke test; only generate SBMLs.",
    )
    args = p.parse_args()

    bng_path = args.bng_path.expanduser()
    ssa_dir = args.ssa_dir.expanduser()
    out_dir = args.out_dir.expanduser()

    if not args.no_generate and not bng_path.is_file():
        sys.exit(f"BNG2.pl not found at {bng_path}")
    if not ssa_dir.is_dir():
        sys.exit(f"SSA bngl source dir not found: {ssa_dir}")

    bng_version = detect_bng_version(bng_path) if not args.no_generate else "(skipped)"

    sources = sorted(ssa_dir.glob("*.bngl"))
    if not sources:
        sys.exit(f"No *.bngl files under {ssa_dir}")

    files: dict[str, dict] = {}
    for src in sources:
        dst = out_dir / f"{src.stem}.xml"
        entry: dict = {"source_bngl": f"benchmarks/models/bngl/ssa/{src.name}"}

        if not args.no_generate:
            if args.force or needs_regen(src, dst, bng_path):
                print(f"[gen]  {src.name} -> {dst.name}", flush=True)
                gen = generate_one(src, dst, bng_path, bng_version)
            else:
                print(f"[skip] {src.name} (up to date)", flush=True)
                gen = {
                    "bng_writeSBML_ok": True,
                    "bng_returncode": 0,
                    "elapsed_sec": None,
                    "skipped_idempotent": True,
                }
            entry.update(gen)

        if not args.no_load_check and dst.exists():
            print(f"[load] {dst.name}", flush=True)
            entry.update(load_check_one(dst))

        files[dst.name] = entry

    payload = {
        "_meta": {
            "bng_path": str(bng_path),
            "bng_version": bng_version,
            "generated": _dt.date.today().isoformat(),
            "n_files": len(files),
        },
        "files": files,
    }
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "load_check.json").write_text(json.dumps(payload, indent=2, sort_keys=False) + "\n")

    failed = [
        n
        for n, v in files.items()
        if v.get("bng_writeSBML_ok") is False or v.get("from_sbml_status") == "error"
    ]
    if failed:
        print(f"\nFAILED: {len(failed)} of {len(files)}: {failed}", file=sys.stderr)
        return 1
    print(f"\nOK: {len(files)} files processed.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

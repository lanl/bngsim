#!/usr/bin/env python3
"""Generate a metapopulation SIR model in BNGL format.

Ring of N_CITIES patches, each with SIR dynamics + transport to neighbors.
Species: S_i, I_i, R_i for i = 0..N_CITIES-1
Local reactions per city: S+I -> 2I (infection), I -> R (recovery)
Transport: S_i <-> S_{i+1}, I_i <-> I_{i+1} (ring topology)

Output: .bngl file (processable by BNG2.pl generate_network -> .net)
        .net file (direct, for quick loading without BNG2.pl)

Usage:
    python gen_metapop.py           # generates both .bngl and .net
    BNG2.pl metapop_sir_100.bngl   # generates .net from .bngl via BNG2.pl
"""

N_CITIES = 100  # 300 species, ~600 reactions
BETA = 0.3  # infection rate
GAMMA = 0.1  # recovery rate
MU = 0.01  # transport rate
S0 = 990  # initial susceptible per city
I0 = 10  # initial infected per city (seed in city 0 only)
R0 = 0  # initial recovered per city

n_sp = 3 * N_CITIES


# ─── Species index helpers (1-based for .net) ────────────────────────────
def sp_S(i):
    return 3 * i + 1


def sp_I(i):
    return 3 * i + 2


def sp_R(i):
    return 3 * i + 3


def generate_bngl(filename):
    """Generate BNGL file that BNG2.pl can process.

    Uses a single molecule type P(state~S~I~R, city~c0~c1~...) so that
    infection (S→I) and recovery (I→R) are state changes on the same
    molecule, avoiding BNG's "state must be set" error with separate types.
    """
    cities = [f"c{i}" for i in range(N_CITIES)]
    city_states = "~".join(cities)

    with open(filename, "w") as f:
        f.write(f"# Metapopulation SIR model — {N_CITIES} cities, ring topology\n")
        f.write(f"# {n_sp} species, ~{2 * N_CITIES + 4 * N_CITIES} reactions (mass-action only)\n")
        f.write("# Graph coloring: 4 colors (75x compression)\n")
        f.write("#\n")
        f.write("# Generated by gen_metapop.py\n\n")

        f.write("begin model\n\n")

        # Parameters
        f.write("begin parameters\n")
        f.write(f"  beta   {BETA:g}   # infection rate\n")
        f.write(f"  gamma  {GAMMA:g}   # recovery rate\n")
        f.write(f"  mu     {MU:g}   # transport rate\n")
        f.write(f"  S0     {S0}   # initial susceptible per city\n")
        f.write(f"  I0     {I0}   # initial infected (city 0 only)\n")
        f.write("end parameters\n\n")

        # Single molecule type: P(state~S~I~R, city~c0~c1~...)
        # This allows infection (S→I) and recovery (I→R) as state changes.
        f.write("begin molecule types\n")
        f.write(f"  P(state~S~I~R,city~{city_states})\n")
        f.write("end molecule types\n\n")

        # Seed species — all cities get S0 susceptible, city 0 gets I0 infected
        f.write("begin seed species\n")
        for i in range(N_CITIES):
            f.write(f"  P(state~S,city~c{i}) S0\n")
        f.write("  P(state~I,city~c0) I0\n")
        f.write("end seed species\n\n")

        # Observables
        f.write("begin observables\n")
        f.write("  Molecules S_total P(state~S)\n")
        f.write("  Molecules I_total P(state~I)\n")
        f.write("  Molecules R_total P(state~R)\n")
        f.write("end observables\n\n")

        # Reaction rules — explicit per-city infection to enforce same-city matching.
        # BNG's pattern P(state~S,city) + P(state~I,city) does NOT enforce that
        # both molecules have the same city state — it generates cross-city reactions.
        # We must write explicit per-city infection rules.
        f.write("begin reaction rules\n")
        f.write("  # Local disease dynamics — explicit per-city infection\n")
        f.write("  # (generic pattern would generate cross-city infections)\n")
        for i in range(N_CITIES):
            f.write(f"  P(state~S,city~c{i}) + P(state~I,city~c{i}) -> ")
            f.write(f"P(state~I,city~c{i}) + P(state~I,city~c{i}) beta\n")
        f.write("\n")
        f.write("  # Recovery (city-independent, pattern matching is fine here)\n")
        f.write("  P(state~I) -> P(state~R) gamma\n")
        f.write("\n")
        f.write("  # Transport between neighboring cities (ring topology)\n")
        for i in range(N_CITIES):
            j = (i + 1) % N_CITIES
            # Transport S
            f.write(f"  P(state~S,city~c{i}) -> P(state~S,city~c{j}) mu\n")
            f.write(f"  P(state~S,city~c{j}) -> P(state~S,city~c{i}) mu\n")
            # Transport I
            f.write(f"  P(state~I,city~c{i}) -> P(state~I,city~c{j}) mu\n")
            f.write(f"  P(state~I,city~c{j}) -> P(state~I,city~c{i}) mu\n")
        f.write("end reaction rules\n\n")

        f.write("end model\n\n")

        f.write("generate_network({overwrite=>1})\n")
        f.write('simulate({method=>"ode", t_end=>200, n_steps=>200})\n')

    print(f"Generated {filename}: {N_CITIES} cities, {n_sp} species")


def generate_net(filename):
    """Generate .net file directly (no BNG2.pl needed)."""
    params = [("beta", BETA), ("gamma", GAMMA), ("mu", MU)]

    species = []
    for i in range(N_CITIES):
        species.append((sp_S(i), f"S_{i}", S0))
        species.append((sp_I(i), f"I_{i}", I0 if i == 0 else 0))
        species.append((sp_R(i), f"R_{i}", R0))

    reactions = []
    rxn_idx = 1
    for i in range(N_CITIES):
        s, inf, r = sp_S(i), sp_I(i), sp_R(i)
        reactions.append((rxn_idx, f"{s},{inf}", f"{inf},{inf}", "beta"))
        rxn_idx += 1
        reactions.append((rxn_idx, f"{inf}", f"{r}", "gamma"))
        rxn_idx += 1

    for i in range(N_CITIES):
        j = (i + 1) % N_CITIES
        reactions.append((rxn_idx, f"{sp_S(i)}", f"{sp_S(j)}", "mu"))
        rxn_idx += 1
        reactions.append((rxn_idx, f"{sp_S(j)}", f"{sp_S(i)}", "mu"))
        rxn_idx += 1
        reactions.append((rxn_idx, f"{sp_I(i)}", f"{sp_I(j)}", "mu"))
        rxn_idx += 1
        reactions.append((rxn_idx, f"{sp_I(j)}", f"{sp_I(i)}", "mu"))
        rxn_idx += 1

    obs_S = ",".join(str(sp_S(i)) for i in range(N_CITIES))
    obs_I = ",".join(str(sp_I(i)) for i in range(N_CITIES))
    obs_R = ",".join(str(sp_R(i)) for i in range(N_CITIES))

    with open(filename, "w") as f:
        f.write("# Metapopulation SIR model\n")
        f.write(
            f"# {N_CITIES} cities, ring topology, {n_sp} species, {len(reactions)} reactions\n\n"
        )

        f.write("begin parameters\n")
        for idx, (name, val) in enumerate(params, 1):
            f.write(f"  {idx} {name} {val:g}\n")
        f.write("end parameters\n\n")

        f.write("begin species\n")
        for idx, name, conc in species:
            f.write(f"  {idx} {name} {conc}\n")
        f.write("end species\n\n")

        f.write("begin reactions\n")
        for idx, reactants, products, rate in reactions:
            f.write(f"  {idx} {reactants} {products} {rate}\n")
        f.write("end reactions\n\n")

        f.write("begin groups\n")
        f.write(f"  1 S_total {obs_S}\n")
        f.write(f"  2 I_total {obs_I}\n")
        f.write(f"  3 R_total {obs_R}\n")
        f.write("end groups\n")

    print(f"Generated {filename}: {n_sp} species, {len(reactions)} reactions")


if __name__ == "__main__":
    # Synthetic metapopulation scaling model -- a dev generator, not part of
    # any paper suite. Output lands in the vendored ODE corpus buckets.
    from pathlib import Path

    _bench = Path(__file__).resolve().parents[1]  # bngsim/benchmarks
    generate_bngl(str(_bench / "models" / "bngl" / "ode" / f"metapop_sir_{N_CITIES}.bngl"))
    generate_net(str(_bench / "models" / "net" / "ode" / f"metapop_sir_{N_CITIES}.net"))

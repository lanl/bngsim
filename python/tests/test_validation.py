class TestSolverDiagnostics:
    """Validate solver_stats and Result metadata."""

    def test_ode_solver_stats(self, data_dir: Path) -> None:
        """ODE run should produce non-trivial solver statistics."""
        result = run_ode(data_dir / "simple_decay.net", t_end=50, n_points=51)
        stats = result.solver_stats

        assert stats["n_steps"] > 0, "Should take at least 1 step"
        assert stats["n_rhs_evals"] > 0, "Should evaluate RHS at least once"

    def test_result_shapes(self, data_dir: Path) -> None:
        """Result arrays should have correct shapes."""
        model = bngsim.Model.from_net(str(data_dir / "two_species_reversible.net"))
        sim = bngsim.Simulator(model, method="ode")
        result = sim.run(t_span=(0, 100), n_points=101)

        assert result.time.shape == (101,)
        assert result.species.shape == (101, 3)  # A, B, C
        assert result.observables.shape[0] == 101
        assert result.observables.shape[1] == 3  # A_free, B_free, C_total

    def test_observable_names(self, data_dir: Path) -> None:
        """Named observable access should work."""
        model = bngsim.Model.from_net(str(data_dir / "simple_decay.net"))
        sim = bngsim.Simulator(model, method="ode")
        result = sim.run(t_span=(0, 10), n_points=11)

        # Observable names from the groups block
        names = result.observable_names
        assert len(names) == 2  # A_tot, B_tot

class TestODEvsRunNetwork:
    """Compare bngsim ODE results against run_network 3.0 reference outputs.

    Each test loads a .net model, runs bngsim ODE, parses the reference .cdat,
    and asserts species concentrations match within documented tolerances.

    Tolerances: max |abs err| < 1e-5, max |rel err| < 1e-6
    (Both solvers use SUNDIALS CVODE with atol=rtol=1e-8, but different versions:
    run_network uses SUNDIALS 2.4, bngsim uses SUNDIALS 7.x)
    """

    def test_simple_decay(self, data_dir: Path) -> None:
        """A → B, k=0.1, A(0)=100. First-order decay."""
        result = run_ode(data_dir / "simple_decay.net", t_end=50, n_points=51)
        ref_time, ref_species = parse_cdat(data_dir / "simple_decay_ref.cdat")
        compare_species(result.species, ref_species, label="simple_decay")

    def test_reversible_binding(self, data_dir: Path) -> None:
        """A + B ↔ C, kf=0.001, kr=0.1, A(0)=100, B(0)=50."""
        result = run_ode(data_dir / "two_species_reversible.net", t_end=1000, n_points=101)
        ref_time, ref_species = parse_cdat(data_dir / "reversible_ref.cdat")
        compare_species(result.species, ref_species, label="reversible")

    def test_saturation_rate_law(self, data_dir: Path) -> None:
        """Functional rate law (formerly Sat): _rateLaw1() = k3/(K4+G)."""
        result = run_ode(data_dir / "saturation.net", t_end=100, n_points=101)
        ref_time, ref_species = parse_cdat(data_dir / "saturation_ref.cdat")
        compare_species(result.species, ref_species, label="saturation")

    def test_time_dependent_function(self, data_dir: Path) -> None:
        """Rate = time()+1. Tests time-dependent functional rate laws.

        NOTE: run_network 3.0 (SUNDIALS 2.4 / muParser) evaluates time()
        differently than bngsim (ExprTk), producing B(t)=t instead of
        B(t)=t²/2+t. The analytical test in TestODEvsAnalytical is the
        authoritative validation for time(). Here we just verify species A
        stays constant (both solvers agree on that).
        """
        result = run_ode(data_dir / "time_dependent_func.net", t_end=10, n_points=21)
        ref_time, ref_species = parse_cdat(data_dir / "time_func_ref.cdat")
        # Only compare species 0 (A), which is constant in both solvers
        compare_species(result.species[:, :1], ref_species[:, :1], label="time_func_A")

    def test_fixed_species(self, data_dir: Path) -> None:
        """$A → B. Fixed (boundary) species A should never decrease."""
        result = run_ode(data_dir / "fixed_species.net", t_end=50, n_points=51)
        ref_time, ref_species = parse_cdat(data_dir / "fixed_species_ref.cdat")
        compare_species(result.species, ref_species, label="fixed_species")

    def test_expression_parameter(self, data_dir: Path) -> None:
        """Species init from ConstantExpression: p=2+3, A(0)=p=5."""
        result = run_ode(data_dir / "expr_param_species.net", t_end=50, n_points=51)
        ref_time, ref_species = parse_cdat(data_dir / "expr_param_ref.cdat")
        compare_species(result.species, ref_species, label="expr_param")

    def test_const_expr_parameter(self, data_dir: Path) -> None:
        """d = d__FREE (ConstantExpression). Linear growth model."""
        result = run_ode(data_dir / "const_expr_setparam.net", t_end=20, n_points=21)
        ref_time, ref_species = parse_cdat(data_dir / "const_expr_ref.cdat")
        compare_species(result.species, ref_species, label="const_expr")



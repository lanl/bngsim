class TestParameterOperations:
    """Validate parameter setting, save/reset concentrations."""

    def test_set_param_changes_dynamics(self, data_dir: Path) -> None:
        """set_param should change rate constant and produce different results."""
        model = bngsim.Model.from_net(str(data_dir / "simple_decay.net"))
        sim = bngsim.Simulator(model, method="ode")

        # Run with original k1=0.1
        r1 = sim.run(t_span=(0, 10), n_points=11)
        A_original = r1.species[-1, 0]

        # Change k1 to 1.0 (10× faster decay)
        model.reset()
        model.set_param("k1", 1.0)
        r2 = sim.run(t_span=(0, 10), n_points=11)
        A_fast = r2.species[-1, 0]

        # Verify: A(10) with k=1.0 should be much smaller
        expected_original = 100 * math.exp(-0.1 * 10)  # ≈ 36.79
        expected_fast = 100 * math.exp(-1.0 * 10)  # ≈ 0.00454

        assert abs(A_original - expected_original) < 1e-4
        assert abs(A_fast - expected_fast) < 1e-4
        assert A_fast < A_original / 1000  # sanity check

    def test_set_param_const_expr(self, data_dir: Path) -> None:
        """set_param on ConstantExpression param should detach expression."""
        model = bngsim.Model.from_net(str(data_dir / "const_expr_setparam.net"))

        # d = d__FREE = 70.0 initially
        # Set d to a new value — should detach from d__FREE
        model.set_param("d", 27.0)

        # Now set d__FREE to something else — d should NOT change
        model.set_param("d__FREE", 999.0)

        # Verify d stayed at 27 (expression detached)
        # The model has counter growing at rate _rateLaw1=1
        # d doesn't affect simulation here, but the param value should be 27
        # We verify indirectly: the model runs without error
        sim = bngsim.Simulator(model, method="ode")
        result = sim.run(t_span=(0, 5), n_points=6)
        assert result.species.shape[0] == 6

    def test_save_reset_concentrations(self, data_dir: Path) -> None:
        """save_concentrations + reset should restore to saved state."""
        model = bngsim.Model.from_net(str(data_dir / "simple_decay.net"))
        sim = bngsim.Simulator(model, method="ode")

        # Run to t=10: A decays from 100 to ~36.79
        r1 = sim.run(t_span=(0, 10), n_points=11)
        r1.species[-1, 0]

        # Save concentrations at this point
        model.save_concentrations()

        # Run further to t=20 (from t=10 state)
        r2 = sim.run(t_span=(0, 10), n_points=11)
        A_at_20 = r2.species[-1, 0]

        # Reset to saved concentrations (t=10 state)
        model.reset()
        r3 = sim.run(t_span=(0, 10), n_points=11)
        A_from_saved = r3.species[-1, 0]

        # A from saved state should match A at t=20
        assert abs(A_from_saved - A_at_20) < 1e-8, (
            f"save/reset mismatch: {A_from_saved} vs {A_at_20}"
        )

    def test_set_concentration(self, data_dir: Path) -> None:
        """set_concentration should change a species' value."""
        model = bngsim.Model.from_net(str(data_dir / "simple_decay.net"))

        # Set A to 50 instead of 100
        model.set_concentration("A()", 50.0)

        sim = bngsim.Simulator(model, method="ode")
        result = sim.run(t_span=(0, 10), n_points=11)

        # A(10) should be 50·exp(-1) ≈ 18.39 (not 100·exp(-1) ≈ 36.79)
        expected = 50 * math.exp(-1.0)
        assert abs(result.species[-1, 0] - expected) < 1e-4, (
            f"A(10) = {result.species[-1, 0]:.4f}, expected {expected:.4f}"
        )



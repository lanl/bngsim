"""Formal validation suite: bngsim vs run_network reference outputs.

This module validates bngsim's ODE and SSA simulators against:
1. Reference .cdat files generated by BioNetGen's run_network 3.0 (SUNDIALS 2.4)
2. Analytical solutions where they exist (gold standard)
3. Statistical properties for stochastic (SSA) simulations

Reference files were generated with:
    run_network -o <prefix> -a 1e-8 -r 1e-8 <model>.net <dt> <n_steps>

Tolerances:
    ODE vs run_network:  max |abs err| < 1e-5, max |rel err| < 1e-6
    ODE vs analytical:   max |abs err| < 1e-6, max |rel err| < 1e-8
    SSA vs analytical:   mean within 3σ over N ensemble runs

PRD §5.5: "Compares against reference outputs from the existing run_network binary"
"""

from __future__ import annotations

import math
from pathlib import Path

import bngsim
import numpy as np

# ─── Helpers ──────────────────────────────────────────────────────────────────


def parse_cdat(path: Path) -> tuple[np.ndarray, np.ndarray]:
    """Parse a BNG .cdat file → (time, species) arrays.

    Returns
    -------
    time : ndarray of shape (n_times,)
    species : ndarray of shape (n_times, n_species)
    """
    rows = []
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            vals = [float(x) for x in line.split()]
            rows.append(vals)
    data = np.array(rows)
    return data[:, 0], data[:, 1:]


def compare_species(
    bngsim_species: np.ndarray,
    ref_species: np.ndarray,
    *,
    atol: float = 1e-5,
    rtol: float = 1e-6,
    label: str = "",
) -> None:
    """Assert bngsim species match reference within tolerances."""
    n_times = min(bngsim_species.shape[0], ref_species.shape[0])
    n_spec = min(bngsim_species.shape[1], ref_species.shape[1])

    max_abs = 0.0
    max_rel = 0.0
    worst_i, worst_j = 0, 0

    for i in range(n_times):
        for j in range(n_spec):
            bval = bngsim_species[i, j]
            rval = ref_species[i, j]
            abs_err = abs(bval - rval)
            rel_err = abs_err / max(abs(rval), 1e-15)

            if abs_err > max_abs:
                max_abs = abs_err
                worst_i, worst_j = i, j
            if rel_err > max_rel:
                max_rel = rel_err

    assert max_abs < atol, (
        f"{label}: max absolute error {max_abs:.2e} exceeds {atol:.0e} "
        f"at time index {worst_i}, species {worst_j}"
    )
    assert max_rel < rtol, f"{label}: max relative error {max_rel:.2e} exceeds {rtol:.0e}"


def run_ode(net_path: Path, t_end: float, n_points: int) -> bngsim.Result:
    """Load model, run ODE, return Result."""
    model = bngsim.Model.from_net(str(net_path))
    sim = bngsim.Simulator(model, method="ode")
    return sim.run(t_span=(0, t_end), n_points=n_points)



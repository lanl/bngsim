class TestSSAValidation:
    """Validate SSA simulator against known statistical properties.

    SSA is stochastic, so we validate:
    1. Ensemble mean converges to ODE solution
    2. Correct propensity calculations (zero species → zero propensity)
    3. Fixed species are not consumed
    4. Homodimerization stat_factor is correct
    """

    def test_simple_decay_ensemble_mean(self, data_dir: Path) -> None:
        """SSA ensemble mean for A→B should match ODE (law of large numbers).

        Run N=200 SSA trajectories, check mean A(t=10) ≈ ODE A(t=10) within 3σ.
        A(t) = 100·exp(-0.1t) → A(10) ≈ 36.79
        Variance of Poisson-like process: σ² ≈ A(t) for large counts.
        """
        model = bngsim.Model.from_net(str(data_dir / "simple_decay.net"))

        N = 200
        A_final = np.zeros(N)
        for i in range(N):
            model.reset()
            sim = bngsim.Simulator(model, method="ssa")
            result = sim.run(t_span=(0, 10), n_points=11, seed=1000 + i)
            A_final[i] = result.species[-1, 0]

        mean_A = np.mean(A_final)
        std_A = np.std(A_final) / np.sqrt(N)  # standard error of mean
        expected = 100 * math.exp(-1.0)  # A(10) = 100·e⁻¹ ≈ 36.79

        assert abs(mean_A - expected) < 3 * std_A + 1.0, (
            f"SSA mean A(10)={mean_A:.2f} ± {std_A:.2f}, expected ≈{expected:.2f} (ODE)"
        )

    def test_ssa_zero_propensity(self, data_dir: Path) -> None:
        """A+B+C → D with C(0)=0: propensity=0, no reaction should fire."""
        model = bngsim.Model.from_net(str(data_dir / "ssa_abc.net"))
        sim = bngsim.Simulator(model, method="ssa")
        result = sim.run(t_span=(0, 100), n_points=11, seed=42)

        # D should remain 0 (no reaction possible)
        assert np.all(result.species[:, 3] == 0), "D should stay 0 with C(0)=0"
        # A and B should be unchanged
        assert np.all(result.species[:, 0] == 5), "A should stay 5"
        assert np.all(result.species[:, 1] == 3), "B should stay 3"

    def test_ssa_fixed_species(self, data_dir: Path) -> None:
        """$A → B with fixed A: A should never decrease in SSA."""
        model = bngsim.Model.from_net(str(data_dir / "fixed_species.net"))
        sim = bngsim.Simulator(model, method="ssa")
        result = sim.run(t_span=(0, 50), n_points=51, seed=42)

        # A is fixed at 100
        assert np.all(result.species[:, 0] == 100), "Fixed species A must stay at 100"
        # B should increase (reactions fire)
        assert result.species[-1, 1] > 0, "B should grow from reactions"

    def test_ssa_homodimer_single_molecule(self, data_dir: Path) -> None:
        """2A → C with A(0)=1: can't pick 2 from 1, propensity=0."""
        model = bngsim.Model.from_net(str(data_dir / "homodimer_ssa.net"))
        sim = bngsim.Simulator(model, method="ssa")
        result = sim.run(t_span=(0, 100), n_points=11, seed=42)

        # C should remain 0 (can't dimerize with 1 molecule)
        assert np.all(result.species[:, 1] == 0), (
            "Homodimerization with 1 molecule should produce 0 product"
        )
        assert np.all(result.species[:, 0] == 1), "A should stay at 1"



# ODE validation: bngsim vs analytical solutions (gold standard)
# ═══════════════════════════════════════════════════════════════════════════════


class TestODEvsAnalytical:
    """Compare bngsim ODE results against exact analytical solutions.

    Analytical solutions are the strongest possible validation — no dependence
    on another numerical solver. Tolerances are tighter than the run_network
    comparison because we're checking against ground truth.

    Tolerances: max |abs err| < 1e-6, max |rel err| < 1e-8
    """

    def test_simple_decay_analytical(self, data_dir: Path) -> None:
        """A → B, k=0.1, A(0)=100.
        Exact: A(t) = 100·exp(-0.1t), B(t) = 100·(1-exp(-0.1t))
        """
        result = run_ode(data_dir / "simple_decay.net", t_end=50, n_points=501)
        t = result.time

        A_exact = 100 * np.exp(-0.1 * t)
        B_exact = 100 * (1 - np.exp(-0.1 * t))
        ref = np.column_stack([A_exact, B_exact])

        compare_species(result.species, ref, atol=1e-5, rtol=1e-6, label="simple_decay_analytical")

    def test_fixed_species_analytical(self, data_dir: Path) -> None:
        """$A → B, k=0.1, A(0)=100 (fixed).
        Exact: A(t) = 100 (constant), B(t) = 0.1·100·t = 10t (linear growth)
        """
        result = run_ode(data_dir / "fixed_species.net", t_end=50, n_points=501)
        t = result.time

        A_exact = np.full_like(t, 100.0)
        B_exact = 10.0 * t  # rate = k * A_fixed = 0.1 * 100 = 10
        ref = np.column_stack([A_exact, B_exact])

        compare_species(
            result.species, ref, atol=1e-6, rtol=1e-8, label="fixed_species_analytical"
        )

    def test_time_function_analytical(self, data_dir: Path) -> None:
        """Rate = time()+1, A(0)=1 (fixed source), B(0)=0.
        dB/dt = (t+1)·A = (t+1)·1
        Exact: B(t) = t²/2 + t
        """
        result = run_ode(data_dir / "time_dependent_func.net", t_end=10, n_points=201)
        t = result.time

        A_exact = np.ones_like(t)
        B_exact = t**2 / 2 + t
        ref = np.column_stack([A_exact, B_exact])

        compare_species(result.species, ref, atol=1e-4, rtol=1e-6, label="time_func_analytical")

    def test_reversible_equilibrium(self, data_dir: Path) -> None:
        """A + B ↔ C, kf=0.001, kr=0.1, A(0)=100, B(0)=50, C(0)=0.
        At equilibrium: Keq = kf/kr = 0.01
        Keq = C_eq / (A_eq · B_eq) = 0.01
        Conservation: A + C = 100, B + C = 50
        Solving: C_eq satisfies 0.01 = C / ((100-C)(50-C))
        """
        result = run_ode(data_dir / "two_species_reversible.net", t_end=1000, n_points=1001)

        # Check equilibrium at final time point
        A_final = result.species[-1, 0]
        B_final = result.species[-1, 1]
        C_final = result.species[-1, 2]

        # Conservation laws
        assert abs((A_final + C_final) - 100) < 1e-6, "Conservation A+C=100 violated"
        assert abs((B_final + C_final) - 50) < 1e-6, "Conservation B+C=50 violated"

        # Equilibrium constant
        Keq = C_final / (A_final * B_final)
        assert abs(Keq - 0.01) < 1e-4, f"Keq={Keq:.6f}, expected 0.01"

    def test_expression_param_init(self, data_dir: Path) -> None:
        """p = 2+3, A(0) = p. Verify A(0) = 5, not 2."""
        result = run_ode(data_dir / "expr_param_species.net", t_end=1, n_points=2)
        assert abs(result.species[0, 0] - 5.0) < 1e-10, (
            f"A(0) = {result.species[0, 0]}, expected 5.0 (expression eval)"
        )


